CREATE TABLE "CAATDemo".contacts (
  contact_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
  first_name VARCHAR2(255 BYTE) NOT NULL,
  last_name VARCHAR2(255 BYTE) NOT NULL,
  email VARCHAR2(255 BYTE) NOT NULL,
  phone VARCHAR2(20 BYTE),
  customer_id NUMBER,
  test01 NUMBER,
  test02 NUMBER,
  PRIMARY KEY (contact_id),
  CONSTRAINT fk_contacts_customers FOREIGN KEY (customer_id) REFERENCES "CAATDemo".customers (customer_id) ON DELETE CASCADE
);